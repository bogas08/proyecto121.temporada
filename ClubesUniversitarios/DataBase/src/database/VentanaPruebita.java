    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            PreparedStatement pps = cn.prepareStatement("INSERT INTO clubuniversitario(id_Club, Nombre, nro_Estudiantes, temaNombre) VALUES (?, ?, ?, ?)");
            pps.setString(1, txtidClub.getText());
            pps.setString(2, txtNombre.getText());
            pps.setString(3, txtNroEst.getText());
            pps.setString(4, txtTema.getText());
            pps.executeUpdate();

            JOptionPane.showMessageDialog(null, "¡Datos guardados!");
            mostrarTabla();
            limpiar();
        } catch (SQLException ex) {
            Logger.getLogger(VentanaPruebita.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int fila = tablaPruebita.getSelectedRow();
        if (fila >= 0) {
            txtidClub.setText(tablaPruebita.getValueAt(fila, 0).toString());
            txtNombre.setText(tablaPruebita.getValueAt(fila, 1).toString());
            txtNroEst.setText(tablaPruebita.getValueAt(fila, 2).toString());
            txtTema.setText(tablaPruebita.getValueAt(fila, 3).toString());
        } else {
            JOptionPane.showMessageDialog(null, "¡Fila no seleccionada!");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            PreparedStatement pps = cn.prepareStatement("UPDATE clubuniversitario SET Nombre = ?, nro_Estudiantes = ?, temaNombre = ? WHERE id_Club = ?");
            pps.setString(1, txtNombre.getText());
            pps.setString(2, txtNroEst.getText());
            pps.setString(3, txtTema.getText());
            pps.setString(4, txtidClub.getText());
            pps.executeUpdate();

            JOptionPane.showMessageDialog(null, "¡Datos actualizados!");
            limpiar();
            mostrarTabla(); 
        } catch (SQLException ex) {
            Logger.getLogger(VentanaPruebita.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tablaPruebita.getSelectedRow();
        if (fila >= 0) {
            String valor = tablaPruebita.getValueAt(fila, 0).toString();
            try {
                PreparedStatement pps = cn.prepareStatement("DELETE FROM clubuniversitario WHERE id_Club = ?");
                pps.setString(1, valor);
                pps.executeUpdate();

                JOptionPane.showMessageDialog(null, "¡Dato eliminado!");
                mostrarTabla();
            } catch (SQLException ex) {
                Logger.getLogger(VentanaPruebita.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "¡Fila no seleccionada!");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

