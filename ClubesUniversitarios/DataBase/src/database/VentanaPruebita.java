    private void mostrarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Nro Estudiantes");
        modelo.addColumn("Tema");

        tablaPruebita.setModel(modelo);

        String sql = "SELECT * FROM clubuniversitario";
        String[] datos = new String[4];

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                datos[0] = rs.getString("id_Club");
                datos[1] = rs.getString("Nombre");
                datos[2] = rs.getString("nro_Estudiantes");
                datos[3] = rs.getString("temaNombre");
                modelo.addRow(datos);
            }
        } catch (SQLException ex) {
            Logger.getLogger(VentanaPruebita.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

